        document.addEventListener("DOMContentLoaded", function() {
            const texts = [
                "Real Time Monitoring",
                "Drone Data Processing",
                "Compliance automation",
                "Data-driven agriculture practices",
                "Drone Training Support",
            ];
            const textClasses = [
                "text-style-1",
                "text-style-2",
                "text-style-3",
                "text-style-4",
                "text-style-5"
            ];
            const textElement = document.getElementById("animatedText");
            let textIndex = 0;
            let charIndex = 0;
            let typingInterval;

            function typeWriter() {
                textElement.className = 'animated-text ' + textClasses[textIndex];
                if (charIndex < texts[textIndex].length) {
                    textElement.innerHTML = texts[textIndex].substring(0, charIndex + 1);
                    charIndex++;
                } else {
                    clearInterval(typingInterval);
                    setTimeout(() => {
                        typingInterval = setInterval(erase, 50);
                    }, 1000); // Delay before erasing starts
                }
            }

            function erase() {
                if (charIndex > 0) {
                    textElement.innerHTML = texts[textIndex].substring(0, charIndex - 1);
                    charIndex--;
                } else {
                    clearInterval(typingInterval);
                    textIndex++;
                    if (textIndex >= texts.length) textIndex = 0;
                    setTimeout(() => {
                        typingInterval = setInterval(typeWriter, 100);
                    }, 500); // Delay before typing starts again
                }
            }

            typingInterval = setInterval(typeWriter, 100);
        });
    </script>
