							<p>In India, the agricultural drone sector is supported by several government schemes aimed at promoting the use of drones in agriculture to enhance efficiency and productivity. Here are a few notable schemes:


</p>
							<p> <b> 1. Kisan Drone Scheme:</b>  Launched by the Ministry of Agriculture and Farmers Welfare, this scheme promotes the use of ‘Kisan Drones’ for crop assessment, digitization of land records, and spraying of insecticides and nutrients.

</p>

<p> <b> 2. Sub-Mission on Agricultural Mechanization (SMAM):</b> This scheme provides financial support for the purchase of drones by agricultural institutes, with grants up to ₹10 lakhs or 100% of the cost of the agriculture drone, whichever is less.Farmer Producer Organizations (FPOs) are eligible to receive a grant of up to 75% of the cost of an agriculture drone for demonstrations on farmers’ fields.


</p>

<p> <b> 3. Drone Didi Scheme:</b>  Announced by Prime Minister Narendra Modi, this scheme aims to train 15,000 female Self-Help Group members to operate and maintain drones, empowering women in rural areas and promoting the use of drones in agriculture, with an outlay of Rs. 1261 Crore from 2024-25 to 2025-26. The scheme will help infusing advance technology in agriculture for improved efficiency, enhance crop yield and reduced cost of operation for the benefit of farmers. 


</p>

<p> <b> 4.  SENSAGRI Project: </b> Initiated by the Indian Council of Agricultural Research (ICAR) through the Indian Agricultural Research Institute (IARI), the SENSAGRI project focuses on developing indigenous prototypes for drone-based crop and soil health monitoring systems. This project is a collaborative effort involving multiple institutes and is funded by the Information Technology Research Academy (ITRA), Department of Electronics and Information Technology1.


</p>
							<p>As for the expected market size, the agricultural drone market in India is projected to grow significantly. According to a report by Frost and Sullivan, the drone usage within the agriculture market is expected to grow at a Compound Annual Growth Rate (CAGR) of 38.5% and reach $121.43 million by 2030. The adoption rate of drones is anticipated to be 2% of the total agriculture machinery spending.
</p>
<p>These schemes and the growing market size indicate a strong push towards integrating advanced technologies like drones into Indian agriculture, which is expected to improve crop yields, reduce labour costs, and increase overall efficiency in the sector. The government’s support through subsidies and training programs is likely to accelerate the adoption of drone technology in agriculture.
</p>
